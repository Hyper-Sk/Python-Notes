# assignment operators 

# assinging value 
x = 5 
print(x)

# plus and equal 
y = 0
y += 3  #same as y = y + 3
print(y)

# minus and equal 
a = 3
a -= 2 # same as a = a - 2 
print(a)

# division and equal to 
d = 10 
d /= 20 #same as d = d / 20
print(d )

# multiply and equal to 
m = 5 
m *= 10 #same as m = m*10
print(m)

# exponentiation or power and equal 
e = 4 
e **= 10 #same as e = e**10

