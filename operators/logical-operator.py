# logical operator 

x = 10
y = 20 
a = 10 
b = 20

# and operator 

if x == a and y == b:
    print('both are equal')
else:
    print('one of both are false')

# or operator 

if x == a or y == a:
    print('one or both are true conditions...')
else:
    print('both conditions are false...')