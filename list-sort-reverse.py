# sorting characters
x = ['orange','apple','grapes','mango','avcardo'] 
x.sort()
print(x)

y = [1,4,2,3,9,6,5]
y.sort()
print(y)


# reverse order 

a = ['orange','apple','grapes','mango','avcardo']
a.sort(reverse=True)
print(a)


b = [1,4,2,3,9,6,5]
b.sort(reverse=True)
print(b)

